// State / territory nomination lists for subclass 190 and 491.
// Each state keeps its OWN occupation list on top of the federal
// MLTSSL / STSOL / ROL gate — an occupation eligible federally may still
// be absent from a given state's fixed list.
//
// Researched 2026-07 directly against each state's official published list
// (URLs below), current for the 2025–26 program year — several programs
// (NSW, QLD, SA, WA) had exhausted their 2025–26 allocation and paused new
// applications at the time of research, with 2026-27 lists not yet
// published; occupation-list *membership* below is the most recently
// published version, not a live application-status feed. Re-verify each
// state's `url` at the start of a new program year.
//
// NSW, QLD, WA, SA, ACT run a genuine fixed occupation list — the arrays
// below are the actual per-occupation 190/491 flags from each state's
// published source, collapsed from their real (multi-stream) program
// structure into a single flag per ANZSCO code. Known simplifications,
// documented per state below: stream-level nuance (e.g. onshore vs
// offshore, small-business carve-outs, graduate-only entries) is not
// modelled — a code counts as eligible if ANY stream lists it.
//
// VIC, TAS and NT do NOT gate 190/491 on a fixed occupation list for the
// common case: they accept any occupation on the relevant federal SOL and
// select competitively (ROI / matrix / ranked rounds). They are handled as
// `openListStates` below. This is a deliberate simplification for NT and
// TAS, whose narrowest offshore-only sub-pathways DO carry a fixed list —
// see the per-territory notes below.
